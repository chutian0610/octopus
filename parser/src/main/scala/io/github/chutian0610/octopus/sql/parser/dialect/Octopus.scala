/**
 * generic sql dialect
 */
class Octopus extends SqlDialect(engine = OCTOPUS) {

  override def partOfIdentifier(c: Char): Boolean = {
    CharMatcher.javaLetterOrDigit().matches(c)
      || c == '_'
      || c == '@'
      || c == ':'
  }

  override def startOfDelimitedIdentifier(c: Char):Boolean ={
    (c == '"') || (c == '`')
  }

  override def startOfIdentifier(c: Char): Boolean = {
    CharMatcher.javaLetterOrDigit().matches(c)
      || c == '_'
  }

  override def matchKeyWord(s: String): Option[KEYWORD] = {
    KEYWORDS.withNameInsensitiveOption(s).filter(x=>Octopus.KeyWordSet.contains(x))
  }


  override def matchReservedWords(s: String): Option[String] = {
    KEYWORDS.withNameInsensitiveOption(s).filter(x => Octopus.ReservedKeyWords.contains(x)).map(x => x.entryName)
  }
}

