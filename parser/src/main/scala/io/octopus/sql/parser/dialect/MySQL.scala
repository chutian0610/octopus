/**
 *
 * @see https://dev.mysql.com/doc/refman/8.0/en/identifiers.html.
 * @see https://dev.mysql.com/doc/refman/8.0/en/keywords.html
 */
class MySQL extends SqlDialect(engine = MYSQL) {
  override def partOfIdentifier(c: Char): Boolean = {
    startOfIdentifier(c)
      || CharMatcher.digit().matches(c)
  }
  override def startOfIdentifier(c: Char): Boolean = {
    CharMatcher.javaLetter().matches(c)
      || c == '_'
      || c == '@'
      || c == '$'
      || CharMatcher.inRange ('\u0080','\uffff').matches(c)
  }
  override def startOfDelimitedIdentifier(c: Char): Boolean = c == '`'

  override def matchKeyWord(s: String): Option[KEYWORD] = {
    KEYWORDS.withNameInsensitiveOption(s).filter(x=>MySQL.KeyWordSet.contains(x))
  }


  override def matchReservedWords(s: String): Option[String] = {
    KEYWORDS.withNameInsensitiveOption(s).filter(x => MySQL.ReservedKeyWords.contains(x)).map(x => x.entryName)
  }
}
